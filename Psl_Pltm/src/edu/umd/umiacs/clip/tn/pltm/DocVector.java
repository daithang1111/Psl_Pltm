package edu.umd.umiacs.clip.tn.pltm;

public class DocVector {

	protected double[] vectors;
	String docName;

	public DocVector(String name, double[] vecs) {
		docName = name;
		vectors = new double[vecs.length];
		for (int i = 0; i < vecs.length; i++) {
			vectors[i] = vecs[i];
		}
	}

	public String getDocName() {
		return docName;
	}

	public void setDocName(String dn) {
		docName = dn;
	}

	public void setVectors(double[] vecs) {
		vectors = new double[vecs.length];
		for (int i = 0; i < vecs.length; i++) {
			vectors[i] = vecs[i];
		}
	}

	public double[] getVectors() {
		return vectors;
	}
}
